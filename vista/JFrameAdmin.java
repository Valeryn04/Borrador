    private void btnRegistroAdminActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroAdminActionPerformed
        
        // Validar campos vacios
        if (!validarCampos()) {
            return;
        }

        Administrador admin = new Administrador(
                jNombre.getText(),
                jApellido.getText(),
                jTel.getText(),
                jComboTipo.getSelectedItem().toString(),
                jDoc.getText()
        );

        RepositorioAdministrador.agregarAdministrador(admin);

        JOptionPane.showMessageDialog(this, "Administrador registrado exitosamente.");

        // Limpiar campos
        jNombre.setText("");
        jApellido.setText("");
        jTel.setText("");
        jDoc.setText("");
        jComboTipo.setSelectedIndex(0);

    }//GEN-LAST:event_btnRegistroAdminActionPerformed

