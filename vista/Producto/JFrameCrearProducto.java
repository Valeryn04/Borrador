    private void guardarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarProductoActionPerformed

        if (!validarFormulario()) {
            return;
        }

        try {
            int id = Integer.parseInt(jTextFieldId.getText());
            String nombre = jTextFieldNombre.getText();
            String descripcion = jTextFieldDescripcion.getText();
            float precio = Float.parseFloat(jTextFieldPrecio.getText());

            if (RepositorioProducto.existeId(id)) {
                JOptionPane.showMessageDialog(this, "El ID ya existe. Por favor, usa uno diferente.");
                return;
            }

            Producto nuevoProducto = new Producto(id, nombre, descripcion, precio);
            RepositorioProducto.agregarProducto(nuevoProducto);

            javax.swing.JOptionPane.showMessageDialog(this, "Producto guardado correctamente");
            limpiarCampos();
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error en los datos numéricos. Verifica el ID y el precio.");
        }
    }//GEN-LAST:event_guardarProductoActionPerformed

    private void limpiarCampos() {
        jTextFieldId.setText("");
        jTextFieldNombre.setText("");
        jTextFieldDescripcion.setText("");
        jTextFieldPrecio.setText("");
    }
