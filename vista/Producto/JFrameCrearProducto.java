    private boolean validarFormulario() {
        // Validar ID
        String idTexto = jTextFieldId.getText();
        if (idTexto.isBlank()) {
            JOptionPane.showMessageDialog(this, "El campo ID no puede estar vacío.");
            return false;
        }
        try {
            int id = Integer.parseInt(idTexto);
            if (id <= 0) {
                JOptionPane.showMessageDialog(this, "El ID debe ser mayor que cero.");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID debe ser un número entero.");
            return false;
        }

        // Validar Nombre
        String nombre = jTextFieldNombre.getText().trim();
        if (nombre.isEmpty() || nombre.length() < 3) {
            JOptionPane.showMessageDialog(this, "El nombre debe tener al menos 3 caracteres.");
            return false;
        }

        // Validar Descripción
        String descripcion = jTextFieldDescripcion.getText().trim();
        if (descripcion.isEmpty()) {
            JOptionPane.showMessageDialog(this, "La descripción no puede estar vacía.");
            return false;
        }

        // Validar Precio
        String precioTexto = jTextFieldPrecio.getText();
        if (precioTexto.isBlank()) {
            JOptionPane.showMessageDialog(this, "El campo Precio no puede estar vacío.");
            return false;
        }
        try {
            float precio = Float.parseFloat(precioTexto);
            if (precio <= 0) {
                JOptionPane.showMessageDialog(this, "El precio debe ser mayor que cero.");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un número válido.");
            return false;
        }

        // Si todo pasó:
        return true;
    }


