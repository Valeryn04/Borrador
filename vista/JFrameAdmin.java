    private boolean validarCampos() {
        String nombre = jNombre.getText().trim();
        String apellido = jApellido.getText().trim();
        String doc = jDoc.getText().trim();
        String tel = jTel.getText().trim();

        // Validar nombre
        if (nombre.isEmpty() || !nombre.matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ\\s]+")) {
            JOptionPane.showMessageDialog(this, "Ingrese un nombre válido (solo letras).");
            return false;
        }

        // Validar apellido
        if (apellido.isEmpty() || !apellido.matches("[a-zA-ZáéíóúÁÉÍÓÚñÑ\\s]+")) {
            JOptionPane.showMessageDialog(this, "Ingrese un apellido válido (solo letras).");
            return false;
        }

        // Validar documento
        if (doc.isEmpty() || !doc.matches("\\d{5,15}")) {
            JOptionPane.showMessageDialog(this, "Ingrese un número de documento válido (solo dígitos, mínimo 5).");
            return false;
        }

        // Validar teléfono
        if (tel.isEmpty() || !tel.matches("\\d{7,10}")) {
            JOptionPane.showMessageDialog(this, "Ingrese un número de teléfono válido (7 a 10 dígitos).");
            return false;
        }

        return true; // Si todo está correcto
    }

