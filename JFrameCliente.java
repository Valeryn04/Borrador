    private void btnRegistroClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroClienteActionPerformed
      String nombre = jNombre.getText();
    String apellido = jApellido.getText();
    String tipoDoc = jComboTipo.getSelectedItem().toString();
    String documento = jDoc.getText();
    String telefono = jTel.getText();

    // Crear el cliente con tipo de documento incluido
    Cliente cliente = new Cliente(nombre, apellido, tipoDoc, documento, telefono);

    // Guardar en el repositorio
    RepositorioCliente.agregarCliente(cliente);

    // Mostrar mensaje
    JOptionPane.showMessageDialog(this, "Cliente registrado con éxito.");

    // Limpiar campos
    jNombre.setText("");
    jApellido.setText("");
    jDoc.setText("");
    jTel.setText("");
    jComboTipo.setSelectedIndex(0);

    }//GEN-LAST:event_btnRegistroClienteActionPerformed

